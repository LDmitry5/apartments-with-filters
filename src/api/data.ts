// Генерация тематических заглушек с номерами квартир
const generateImage = (id: string, width = 400, height = 250, index = 0) => {
  // Разные цвета для разных квартир
  const colors = [
    "3b82f6", // blue-500
    "10b981", // emerald-500
    "f59e0b", // amber-500
    "8b5cf6", // violet-500
    "ec4899", // pink-500
    "14b8a6", // teal-500
    "f97316", // orange-500
    "06b6d4", // cyan-500
  ];

  // Хэш из ID для выбора цвета
  const hash = id.split("").reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const colorIndex = (hash + index) % colors.length;
  const bgColor = colors[colorIndex];

  // Номер квартиры для отображения
  const aptNum = id.replace("apt-", "");
  const text = index === 0 ? `ЖК+${aptNum}` : `Фото+${index + 1}`;

  return `https://placehold.co/${width}x${height}/${bgColor}/ffffff?text=${text}&font=roboto`;
};

